              <p>
                Listed below is some helpful terminology when navigating Blackboard Learn and our Humber help documents.
              </p>

              <h4>Action Bar</h4>
              <p>The <strong>Action Bar</strong> is a row at the top of a content area or course tool page containing page-level actions such as <strong>Build Content</strong>, <strong>Assessment</strong>, <strong>Create Column</strong>, and <strong>Search</strong>.<br>
               <img src="../assets/images/instructors/getting_started/blackboard_terminology/01.jpg" alt"" />
             </p>
             <hr>

             <h4>Action Link</h4>
             <p>An <strong>Action Link</strong>, sometimes called a dropdown arrow or dropdown menu, is the small button that appears beside the names of items when you hover over them. It gives you options to <strong>Edit</strong>, <strong>Delete</strong>, and <strong>Hide</strong> the item amongst other options. <br>
               <img src="../assets/images/instructors/getting_started/blackboard_terminology/02.jpg" alt"" />
             </p>
             <hr>

             <h4>Breadcrumbs Path</h4>
             <p>The <strong>Breadcrumbs Path</strong> or <strong>Orientation Bar</strong> is the trail created at the top of your page as you navigate through Blackboard. You can use it to go back to previous pages and expand the trail by clicking on the ellipsis (…).<br>
               <img src="../assets/images/instructors/getting_started/blackboard_terminology/03.jpg" alt"" />
             </p>
             <hr>

             <h4>Content Area</h4>
             <p>A <strong>Content Area</strong> is a page that contains links to content that the instructor has added through the <strong>Build Content</strong> and <strong>Assessment</strong> tools on the <strong>Action Bar</strong>. <strong>Content Areas</strong> are added as links on the <strong>Course Menu</strong>.</p>
             <hr>

             <h4>Course ID</h4>
             <p>The <strong>Course ID</strong> is the unique identifier for the course including a six digit course number, underscores (_), and hyphens (-). It can be found at the top of your <strong>Course Menu</strong>.<br>
               <img src="../assets/images/instructors/getting_started/blackboard_terminology/05.jpg" alt"" />
             </p>
             <hr>

             <h4>Course Link</h4>
             <p>A <strong>Course Link</strong> is a shortcut to an existing item in your course including <strong>Content Areas</strong> and <strong>Course Tools</strong>.</p>
             <hr>

             <h4>Course Menu</h4>
             <p>The <strong>Course Menu</strong>, sometimes called the <strong>Sidebar</strong>, is the vertical menu on the left side of the course window. The <strong>Course Menu</strong> allows you to access <strong>Content Areas</strong>, <strong>Course Tools</strong>, and the <strong>Control Panel</strong>.</p>
             <hr>

             <h4>Edit Mode</h4>
             <p>Blackboard allows you to switch between <strong>Edit Mode: ON</strong>, where you can view and interact with the course as an <strong>Instructor</strong>, and <strong>Edit Mode: OFF</strong>, where you can view the course as a <strong>Student</strong>. To change <strong>Edit Mode</strong>, click the button at the top right of the course window.<br>
               <img src="../assets/images/instructors/getting_started/blackboard_terminology/08.jpg" alt"" />
             </p>



