              <h2>Course Copy</h2>
              <div class="downloads">
                <!--<p><a href="#course_copy_vid"><span class="icon-video"></span> Video</a></p>-->
                <p><a href="../assets/_source/instructors/getting_started/course_copy/course_copy.pdf" target="_blank"><span class="icon-book"></span> PDF</a></p>
                <div class="clearfix"></div>
              </div>

              <p>You can copy content from an old course with content into a new course (which is typically blank) using the Course Copy option.</p>
              <p><span class="bold">NOTE:</span> Be <span class="bold">very careful</span> when performing a course copy! It takes <span class="bold">just seconds</span> to <span class="bold">corrupt</span> valuable content if the course copy is.</p>
              <ul>
                <li>Done in the wrong direction.</li>
                <li>Done from/into the wrong course (wrong year, wrong semester).</li>
                <li>Done multiple times into the same course.</li>
              </ul>

              <h3>Requirements</h3>
              <ul>
                <li>Do not perform a "course copy" <span class="bold">multiple times</span> into the same course. The "course copy" process is an additive which means it leaves in place whatever content is already in the destination course.</li>
              </ul>

              <h3>Copy your course</h3>
              <ol>
                <li>Login to the course <span class="bold">containing content.</span></li>
                <li><span class="bold">Verify</span> you are in the right course. (check semester, year, course number, and source content)</li>
                <li>Under Control Panel, expand <span class="bold">Packages and Utilities</span>, select <span class="bold">Course Copy.</span></li>
                
                <img src="../assets/images/instructors/getting_started/course_copy/03.png" alt"" />
                
                <li>For <span class="bold">Destination Course ID</span>, click on the <span class="bold">Browse</span> button to select the course where you want to add content. Verify that you have selected the correct course. Check semester, year, course number, typically blank course.</span>
                  <br><br>

                <img src="../assets/images/instructors/getting_started/course_copy/04.png" alt"" />
                </li>
                <li>Click on <span class="bold">Select All</span> to copy your course content to the destination course</li>
                <li>Uncheck <span class="bold">Announcements</span> because you do not want to copy old announcements over to your new course</li>
                <li>Under Discussion Board, select the second option: <span class="italic">Include only the forums with no starter posts</span></li>
                <li>You may also uncheck Retention Center Rules if you are planning to create new rules</li>
                <li>Leave <span class="bold">Availability</span> unchecked
                  <br><br>

                <img src="../assets/images/instructors/getting_started/course_copy/09.png" alt"" />
                </li>
                <li>Under <span class="bold">FILE ATTACHMENTS</span>, select the third option: <span class="italic">Copy links and copies of the content (include entire course home folder)</span><br><br>

                  <img src="../assets/images/instructors/getting_started/course_copy/10.png" alt"" />
                </li>
                <li>Lastly, click on the Submit button</li>
                <li>You will receive an email when the copy process is complete.
                  </li>
              </ol>

              <p>Blackboard displays a <span class="bold">"Success: course copy action queued"</span> message. This means the copy is not complete, but the request has been sent to the server.</p>
              
              <p><img src="../assets/images/instructors/getting_started/course_copy/12.png" alt"" /></p>

               <p> After receiving the completion email, you may need to log out and wait a minimum of 15 minutes before the destination course will show all of the copied content. If your course contains large files, the copy process will take even longer.
              </p>

              <h3>Edit and clean-up copied results</h3>
              <p>Old content such as discussion posts, and announcements will have copied over from the original course and should be deleted within the new course.</p>
              <ul>
                <li>Delete old discussion posts</li>
                <li>Delete old announcements</li>
                <li>Update due dates on tests</li>
                <li>Update due dates on assignments</li>
                <li>Replace files</li>
               </ul>

