                  <h2>Virtual Proctoring - A Summary</h2>
                  <p>With Virtual Proctoring, eligible online students can now take their online final exam from the comfort of their own home provided that they meet the minimum distance requirement of living more than 100 km away from Humber College. A small fee does apply, depending on the length of the exam; however, virtual proctoring gives our students an easy and flexible alternative as opposed to completing their final exams at another educational institution.</p>
                  <p>For additional details about virtual proctoring, <a href="#" data-reveal-id="virtual_proctoring_video">please view the following video</a></p>
                  
                  <p>
                    Eligible students are asked to fill out the Virtual Proctoring form, available at <a href="http://www.humber.ca/onlinelearning/proctor.php" target="_blank">http://www.humber.ca/onlinelearning/virtualproctor.php</a> at least 6 weeks prior to their final exam.<br />
                    Students must have a high speed internet connection and a good quality webcam in order to opt for the Virtual Proctoring option. Exams are supervised in a fully secure environment by a third party located in the greater GTA. The live proctor to student ratio is 1:4
                  </p>

                  <h2>Environment Verification via Video</h2>
                  <p>Using the webcam, a student's immediate surroundings are monitored to ensure that students are taking the exam without any distractions, course notes or other aids. Students are asked use their webcam to take video footage of all corners of their space before the exam begins so that the proctor can validate the student's immediate surroundings. The live proctor checks identification and supervises the entire exam via webcam and with remote access to the student's computer. All virtual proctoring sessions are recorded and archived.</p>

                  <h2>Student Authentication</h2>
                  <p>Students are required to provide at least two forms of appropriate photo ID that corresponds with their registration information once they have logged into the virtual meeting room. If the student has properly registered for the course, the virtual proctor will have the student's name, address and exam details. Visual confirmation of the student is mandatory.</p>

                  <h2>Faculty Requirements</h2>
                  <p>Faculty will be asked to give information stating the exam date, length of time, and an exam password. An exam password is necessary as all virtually proctored exams are done through our Blackboard LMS. Faculty will be required to create/upload their final exam into their Blackboard environment as online assessments under the Assessments tab/menu option. For helpful documents and videos on how to create exams on Blackboard and how to password protect your exam, please refer to the link below.</p>

                  <p>If you have any questions regarding the setting up of a Virtual Proctoring session, please contact an <span class="bold">Open Learning Centre (OLC) Student Support Advisor at 416 675 5049</span></p>
                        
                  <p>If you need technical support on uploading your exam, please contact the Waterloo Co-op students at our North (ext. 4744) or Lakeshore (ext. 3288) Studios --
